package com.passx.question.passx;

public class DataPassX {

    private int Hinhanh;
    private String Ten;

    public DataPassX(int hinhanh, String ten) {
        Hinhanh = hinhanh;
        Ten = ten;
    }

    public int getHinhanh() {
        return Hinhanh;
    }

    public void setHinhanh(int hinhanh) {
        Hinhanh = hinhanh;
    }

    public String getTen() {
        return Ten;
    }

    public void setTen(String ten) {
        Ten = ten;
    }
}
